import java.util.*;
class Relational
{
	public static void main(String args[])
	{
		int a;
		int b;
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the values of a,b: ");
		a=sc.nextInt();
		b=sc.nextInt();
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);	
}		
}
